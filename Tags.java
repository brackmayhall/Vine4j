public class Tags {
	
	/**
	 * the tag name.
	 */
   	private String tag;
   	
   	/**
   	 * the tage id.
   	 */
   	private Number tagId;

	/**
	 * @return the tag
	 */
	public final String getTag() {
		return tag;
	}

	/**
	 * @param tag the tag to set
	 */
	public final void setTag(final String tag) {
		this.tag = tag;
	}

	/**
	 * @return the tagId
	 */
	public final Number getTagId() {
		return tagId;
	}

	/**
	 * @param tagId the tagId to set
	 */
	public final void setTagId(final Number tagId) {
		this.tagId = tagId;
	}
   	

}
